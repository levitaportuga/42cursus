#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
	{
		ft_putnbr_fd((n / 10), 1);
		write(fd, "8", 1);
	}
	else if (n < 0)
	{
		write (fd, "-", 1);
		ft_putnbr_fd(-n, 1);
	}
	else
	{
		if (n > 9)
		{
			ft_putnbr_fd((n / 10), 1);
		}
		n = 48 + n % 10;
		write (fd, &n, 1);
	}
}
